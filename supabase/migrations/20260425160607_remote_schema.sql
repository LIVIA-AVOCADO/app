CREATE OR REPLACE FUNCTION public.search_messages_by_content(p_tenant_id uuid, p_query text, p_limit integer DEFAULT 20, p_offset integer DEFAULT 0)
 RETURNS TABLE(message_id uuid, conversation_id uuid, message_snippet text, message_timestamp timestamp with time zone, contact_id uuid, contact_name text, contact_phone text, conversation_status text)
 LANGUAGE sql
 STABLE
 SET search_path TO 'public'
AS $function$
  SELECT
    m.id                    AS message_id,
    m.conversation_id       AS conversation_id,
    substring(
      m.content
      FROM GREATEST(1, position(lower(p_query) IN lower(m.content)) - 40)
      FOR 120
    )                       AS message_snippet,
    m.timestamp             AS message_timestamp,
    ct.id                   AS contact_id,
    ct.name                 AS contact_name,
    ct.phone                AS contact_phone,
    c.status::text          AS conversation_status
  FROM public.messages m
  INNER JOIN public.conversations c
    ON c.id = m.conversation_id
   AND c.tenant_id = p_tenant_id
  INNER JOIN public.contacts ct
    ON ct.id = c.contact_id
  WHERE m.content ILIKE ('%' || p_query || '%')
    AND length(p_query) >= 3
  ORDER BY m.timestamp DESC
  LIMIT  p_limit
  OFFSET p_offset;
$function$
;

