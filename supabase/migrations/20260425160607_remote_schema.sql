CREATE OR REPLACE FUNCTION public.get_active_tenants_with_reactivation_config()
 RETURNS TABLE(tenant_id uuid, tenant_name character varying, tenant_is_active boolean, tenant_ia_active boolean, cnpj character varying, phone character varying, plan character varying, has_reactivation_settings boolean, reactivate_when_ia_active_false boolean, step_configuration boolean, steps jsonb, exhausted_action public.tenant_reactivation_fallback_action, exhausted_message text, max_reactivation_window_minutes integer, max_window_action public.tenant_reactivation_fallback_action, max_window_message text, settings_created_at timestamp with time zone, settings_updated_at timestamp with time zone)
 LANGUAGE sql
 STABLE
AS $function$
  with steps_by_tenant as (
    select
      trs.tenant_id,
      count(*)::bigint as step_count,
      jsonb_agg(
        jsonb_build_object(
          'id', trs.id,
          'sequence', trs.sequence,
          'wait_time_minutes', trs.wait_time_minutes,
          'action_type', trs.action_type,
          'action_parameter', trs.action_parameter,
          'start_time', trs.start_time,
          'end_time', trs.end_time,
          'created_at', trs.created_at,
          'updated_at', trs.updated_at
        )
        order by trs.sequence asc, trs.created_at asc, trs.id asc
      ) as steps
    from public.tenant_reactivation_rules_steps trs
    group by trs.tenant_id
  )
  select
    t.id as tenant_id,
    t.name as tenant_name,
    t.is_active as tenant_is_active,
    t.ia_active as tenant_ia_active,
    t.cnpj,
    t.phone,
    t.plan,

    true as has_reactivation_settings,
    true as reactivate_when_ia_active_false,

    (coalesce(sb.step_count, 0) > 0) as step_configuration,
    coalesce(sb.steps, '[]'::jsonb) as steps,

    s.exhausted_action,
    s.exhausted_message,
    s.max_reactivation_window_minutes,
    s.max_window_action,
    s.max_window_message,
    s.created_at as settings_created_at,
    s.updated_at as settings_updated_at

  from public.tenants t
  inner join public.tenant_reactivation_settings s
    on s.tenant_id = t.id
  left join steps_by_tenant sb
    on sb.tenant_id = t.id

  where
    t.is_active = true
    and coalesce(s.reactivate_when_ia_active_false, false) = true

  order by t.name;
$function$
;

