CREATE OR REPLACE FUNCTION public.get_contact_and_open_conversation(p_channel_id uuid, p_tenant_id uuid, p_external_identification_contact text)
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  v_contact public.contacts%rowtype;
  v_conversation public.conversations%rowtype;
begin
  -- 1) Busca o contato
  select c.*
    into v_contact
  from public.contacts c
  where c.tenant_id = p_tenant_id
    and c.external_identification_contact = p_external_identification_contact
  order by c.updated_at desc
  limit 1;

  -- 2) Busca a conversa open mais recente desse contato no canal/tenant
  if v_contact.id is not null then
    select cv.*
      into v_conversation
    from public.conversations cv
    where cv.tenant_id = p_tenant_id
      and cv.channel_id = p_channel_id
      and cv.contact_id = v_contact.id
      and cv.status = 'open'
    order by cv.last_message_at desc, cv.updated_at desc
    limit 1;
  end if;

  -- 3) Retorno padronizado
  return jsonb_build_object(
    'contact',
    case
      when v_contact.id is not null then to_jsonb(v_contact)
      else '{}'::jsonb
    end,
    'conversation',
    case
      when v_conversation.id is not null then to_jsonb(v_conversation)
      else '{}'::jsonb
    end
  );
end;
$function$
;

