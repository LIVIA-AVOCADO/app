CREATE OR REPLACE FUNCTION public.get_conversations_markdown_paginated(p_tenant_id uuid, p_limit integer DEFAULT 50, p_min_messages integer DEFAULT 10, p_cursor_conversation_id uuid DEFAULT NULL::uuid)
 RETURNS TABLE(conversation_id uuid, last_message_at timestamp with time zone, total_messages bigint, contact_name text, contact_phone text, channel_name text, channel_provider_name text, conversation_markdown text)
 LANGUAGE sql
 STABLE
AS $function$
with cursor_data as (
  select
    c.id as cursor_conversation_id,
    c.last_message_at as cursor_last_message_at
  from public.conversations c
  where c.id = p_cursor_conversation_id
    and c.tenant_id = p_tenant_id
),
conversation_message_counts as (
  select
    c.id as conversation_id,
    count(m.id) as total_messages
  from public.conversations c
  join public.messages m
    on m.conversation_id = c.id
  where c.tenant_id = p_tenant_id
  group by c.id
  having count(m.id) > p_min_messages
),
target_conversations as (
  select
    c.id,
    c.contact_id,
    c.channel_id,
    c.status,
    c.external_id,
    c.last_message_at,
    c.created_at,
    cmc.total_messages
  from public.conversations c
  join conversation_message_counts cmc
    on cmc.conversation_id = c.id
  where c.tenant_id = p_tenant_id
    and (
      p_cursor_conversation_id is null
      or (c.last_message_at, c.id) < (
        (select cd.cursor_last_message_at from cursor_data cd),
        (select cd.cursor_conversation_id from cursor_data cd)
      )
    )
  order by c.last_message_at desc, c.id desc
  limit p_limit
)
select
  tc.id as conversation_id,
  tc.last_message_at,
  tc.total_messages,
  ct.name::text as contact_name,
  ct.phone::text as contact_phone,
  ch.name::text as channel_name,
  cp.name::text as channel_provider_name,
  format(
$md$
# Conversa: %s

- **Contato:** %s (%s)
- **Canal:** %s
- **Provider:** %s
- **Status:** %s
- **Última mensagem:** %s
- **Qtd. mensagens:** %s

## Mensagens
%s
$md$,
    tc.id,
    coalesce(ct.name, 'Sem nome'),
    coalesce(ct.phone, 'Sem telefone'),
    coalesce(ch.name, 'Sem canal'),
    coalesce(cp.name, 'Sem provider'),
    coalesce(tc.status::text, 'sem status'),
    to_char(tc.last_message_at at time zone 'America/Sao_Paulo', 'DD/MM/YYYY HH24:MI:SS'),
    tc.total_messages,
    coalesce(
      string_agg(
        format(
          '- **[%s] [%s] [%s]** %s',
          to_char(m.timestamp at time zone 'America/Sao_Paulo', 'DD/MM/YYYY HH24:MI:SS'),
          coalesce(m.sender_type::text, 'unknown'),
          coalesce(m.message_type::text, 'text'),
          coalesce(
            nullif(regexp_replace(m.content, E'[\\r\\n]+', ' ', 'g'), ''),
            '[sem conteúdo]'
          )
        ),
        E'\n'
        order by m.timestamp asc, m.created_at asc, m.id asc
      ),
      '- [sem mensagens]'
    )
  ) as conversation_markdown
from target_conversations tc
join public.contacts ct
  on ct.id = tc.contact_id
left join public.channels ch
  on ch.id = tc.channel_id
left join public.channel_providers cp
  on cp.id = ch.channel_provider_id
left join public.messages m
  on m.conversation_id = tc.id
group by
  tc.id,
  tc.last_message_at,
  tc.created_at,
  tc.total_messages,
  tc.status,
  ct.name,
  ct.phone,
  ch.name,
  cp.name
order by tc.last_message_at desc, tc.id desc;
$function$
