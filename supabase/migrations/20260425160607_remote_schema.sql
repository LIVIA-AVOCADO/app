CREATE OR REPLACE FUNCTION public.add_tag_to_conversation_if_not_exists(p_conversation_id uuid, p_tag_id uuid)
 RETURNS jsonb
 LANGUAGE plpgsql
AS $function$
declare
  v_inserted boolean;
  v_tag public.tags%rowtype;
begin
  insert into public.conversation_tags (
    conversation_id,
    tag_id
  )
  values (
    p_conversation_id,
    p_tag_id
  )
  on conflict (conversation_id, tag_id) do nothing;

  v_inserted := found;

  select *
    into v_tag
  from public.tags
