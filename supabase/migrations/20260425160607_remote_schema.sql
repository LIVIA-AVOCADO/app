CREATE OR REPLACE FUNCTION public.guard_ia_active_on_schedule()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_result jsonb;
BEGIN
  -- Só actua quando ia_active = true está sendo definido
  IF NEW.ia_active IS NOT TRUE THEN
    RETURN NEW;
  END IF;

  -- Sem tenant_id não há schedule para checar
  IF NEW.tenant_id IS NULL THEN
    RETURN NEW;
  END IF;

  -- Consulta o schedule actual do tenant
  SELECT public.is_agent_online(NEW.tenant_id) INTO v_result;

  -- Se offline → força ia_active = false e registra o motivo
  IF (v_result->>'online')::boolean = false THEN
    NEW.ia_active   := false;
    NEW.pause_notes := 'IA inativa — fora do horário configurado [schedule]';
  END IF;

  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.handle_agent_schedule_transitions()
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_tenant    record;
  v_result    jsonb;
BEGIN
  -- Itera sobre tenants que possuem configuração de horário.
  FOR v_tenant IN
    SELECT DISTINCT tenant_id
    FROM public.agent_schedule_weekly
    WHERE is_active = true
  LOOP
    -- Verifica se o agente está offline agora para este tenant.
    SELECT public.is_agent_online(v_tenant.tenant_id) INTO v_result;

    IF (v_result->>'online')::boolean = false THEN
      -- Pausa IA em todas as conversas abertas do tenant que ainda estão com ia_active = true.
      UPDATE public.conversations
      SET
        ia_active   = false,
        pause_notes = 'IA pausada automaticamente por horário [schedule]',
        updated_at  = now()
      WHERE
        tenant_id = v_tenant.tenant_id
        AND status    = 'open'
        AND ia_active = true;
    END IF;
  END LOOP;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.is_agent_online(p_tenant_id uuid)
 RETURNS jsonb
 LANGUAGE plpgsql
 STABLE SECURITY DEFINER
AS $function$
DECLARE
  v_timezone         text;
  v_now_local        timestamptz;
  v_now_time         time;
  v_today            date;
  v_dow              smallint;
  v_has_schedule     boolean;
  v_exception        record;
  v_interval         record;
  v_offline_msg      text;
BEGIN
  -- 1. Verifica se o tenant tem alguma configuração de horário.
  SELECT EXISTS (
    SELECT 1 FROM public.agent_schedule_weekly
    WHERE tenant_id = p_tenant_id AND is_active = true
    LIMIT 1
  ) INTO v_has_schedule;

  IF NOT v_has_schedule THEN
    -- Sem configuração = 24/7, sem custo adicional.
    RETURN jsonb_build_object('online', true, 'reason', '24_7');
  END IF;

  -- 2. Determina timezone do tenant (primeiro registro encontrado).
  SELECT timezone INTO v_timezone
  FROM public.agent_schedule_weekly
  WHERE tenant_id = p_tenant_id AND is_active = true
  LIMIT 1;

  v_timezone  := COALESCE(v_timezone, 'America/Sao_Paulo');
  v_now_local := now() AT TIME ZONE v_timezone;
  v_today     := v_now_local::date;
  v_now_time  := v_now_local::time;
  v_dow       := EXTRACT(DOW FROM v_now_local)::smallint; -- 0=dom, 6=sab

  -- 3. Verifica exceções para hoje.
  SELECT * INTO v_exception
  FROM public.agent_schedule_exceptions
  WHERE tenant_id = p_tenant_id
    AND exception_date = v_today
  LIMIT 1;

  IF FOUND THEN
    IF v_exception.type = 'blocked' THEN
      RETURN jsonb_build_object(
        'online',          false,
        'reason',          'exception_blocked',
        'offline_message', NULL
      );
    END IF;

    -- type = 'custom': verifica se agora está dentro do intervalo custom.
    IF v_exception.type = 'custom'
       AND v_now_time >= v_exception.start_time
       AND v_now_time <  v_exception.end_time
    THEN
      RETURN jsonb_build_object('online', true, 'reason', 'exception_custom');
    ELSE
      RETURN jsonb_build_object(
        'online',          false,
        'reason',          'exception_custom_outside',
        'offline_message', NULL
      );
    END IF;
  END IF;

  -- 4. Verifica horário semanal para o dia de hoje.
  FOR v_interval IN
    SELECT start_time, end_time, offline_message
    FROM public.agent_schedule_weekly
    WHERE tenant_id  = p_tenant_id
      AND day_of_week = v_dow
      AND is_active   = true
    ORDER BY start_time
  LOOP
    IF v_now_time >= v_interval.start_time
       AND v_now_time < v_interval.end_time
    THEN
      RETURN jsonb_build_object('online', true, 'reason', 'weekly_schedule');
    END IF;
    -- Guarda a mensagem do último intervalo como fallback (mais representativa).
    v_offline_msg := v_interval.offline_message;
  END LOOP;

  -- Fora de todos os intervalos do dia (ou dia sem intervalos configurados).
  RETURN jsonb_build_object(
    'online',          false,
    'reason',          'outside_schedule',
    'offline_message', v_offline_msg
  );
END;
$function$
;

CREATE OR REPLACE FUNCTION public.livechat_conversation_status_counts(p_tenant_id uuid)
 RETURNS TABLE(ia_count bigint, manual_count bigint, closed_count bigint, important_count bigint, unread_manual_count bigint)
 LANGUAGE sql
 STABLE
 SET search_path TO 'public'
AS $function$
  SELECT
    COUNT(*) FILTER (
      WHERE c.ia_active IS TRUE AND c.status IS DISTINCT FROM 'closed'
    )::bigint AS ia_count,
    COUNT(*) FILTER (
      WHERE c.ia_active IS NOT TRUE AND c.status IS DISTINCT FROM 'closed'
    )::bigint AS manual_count,
    COUNT(*) FILTER (WHERE c.status = 'closed')::bigint AS closed_count,
    COUNT(*) FILTER (
      WHERE c.is_important IS TRUE AND c.status IS DISTINCT FROM 'closed'
    )::bigint AS important_count,
    COUNT(*) FILTER (
      WHERE c.ia_active IS NOT TRUE
        AND c.status IS DISTINCT FROM 'closed'
        AND c.has_unread IS TRUE
    )::bigint AS unread_manual_count
  FROM public.conversations c
  INNER JOIN public.contacts ct
    ON ct.id = c.contact_id
   AND ct.tenant_id = c.tenant_id
  WHERE c.tenant_id = p_tenant_id
    AND COALESCE(ct.is_muted, false) = false;
$function$
;

