CREATE OR REPLACE FUNCTION public.get_funil_data(p_tenant_id uuid, p_days_ago integer DEFAULT 30, p_channel_id uuid DEFAULT NULL::uuid, p_start_date timestamp without time zone DEFAULT NULL::timestamp without time zone, p_end_date timestamp without time zone DEFAULT NULL::timestamp without time zone)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
DECLARE
  v_start_date TIMESTAMP;
  v_end_date TIMESTAMP;
  v_result JSON;
BEGIN
  -- Calculate date range
  IF p_start_date IS NOT NULL AND p_end_date IS NOT NULL THEN
    v_start_date := p_start_date;
    v_end_date := p_end_date;
  ELSE
    v_end_date := CURRENT_TIMESTAMP;
    v_start_date := v_end_date - (p_days_ago || ' days')::INTERVAL;
  END IF;

  WITH

  -- ================================================================
  -- BASE DATA: Conversations
  -- Filtra por updated_at para capturar conversas com atividade no período
  -- (inclui conversas antigas que foram atualizadas, pausadas ou fechadas)
  -- ================================================================
  base_conversations AS (
    SELECT
      c.id,
      c.tenant_id,
      c.status,
      c.ia_active,
      c.created_at,
      c.updated_at
    FROM conversations c
    WHERE c.tenant_id = p_tenant_id
      AND c.updated_at >= v_start_date
      AND c.updated_at <= v_end_date
      AND (p_channel_id IS NULL OR c.channel_id = p_channel_id)
  ),

  -- ================================================================
  -- KPIs: Funnel metrics
  -- ================================================================
  funnel_kpis AS (
    SELECT
      -- Status breakdown
      COUNT(*) FILTER (WHERE status = 'open')::INTEGER AS "conversationsOpen",
      COUNT(*) FILTER (WHERE status = 'paused')::INTEGER AS "conversationsPaused",
      COUNT(*) FILTER (WHERE status = 'closed')::INTEGER AS "conversationsClosed",
      
      -- Conversion rate (open to closed)
      ROUND(
        (COUNT(*) FILTER (WHERE status = 'closed')::DECIMAL / NULLIF(COUNT(*), 0)) * 100,
        1
      ) AS "conversionRate",
      
      -- Average time to pause (in seconds)
      ROUND(
        AVG(EXTRACT(EPOCH FROM (updated_at - created_at))) FILTER (WHERE status = 'paused')
      )::INTEGER AS "avgTimeToPauseSeconds",
      
      -- Average time to close (in seconds)
      ROUND(
        AVG(EXTRACT(EPOCH FROM (updated_at - created_at))) FILTER (WHERE status = 'closed')
      )::INTEGER AS "avgTimeToCloseSeconds"
      
    FROM base_conversations
  ),

  -- ================================================================
  -- STATUS EVOLUTION: Time series of status over time
  -- Agrupa por data de atualização para mostrar atividade no período
  -- ================================================================
  status_evolution AS (
    SELECT COALESCE(
      json_agg(
        json_build_object(
          'date', day::TEXT,
          'open', COALESCE(open_count, 0),
          'paused', COALESCE(paused_count, 0),
          'closed', COALESCE(closed_count, 0)
        )
        ORDER BY day
      ),
      '[]'::json
    ) AS data
    FROM (
      SELECT
        DATE(updated_at) AS day,
        COUNT(*) FILTER (WHERE status = 'open')::INTEGER AS open_count,
        COUNT(*) FILTER (WHERE status = 'paused')::INTEGER AS paused_count,
        COUNT(*) FILTER (WHERE status = 'closed')::INTEGER AS closed_count
      FROM base_conversations
      GROUP BY DATE(updated_at)
      ORDER BY day
    ) daily_status
  ),

  -- ================================================================
  -- PAUSE REASONS: Real data from database
  -- ================================================================
  pause_reasons AS (
    SELECT COALESCE(
      json_agg(
        json_build_object(
          'reason', reason,
          'count', count,
          'percentage', ROUND((count::DECIMAL / NULLIF(total, 0)) * 100, 1)
        )
        ORDER BY count DESC
      ),
      '[]'::json
    ) AS data
    FROM (
      SELECT
        COALESCE(r.description, 'Não especificado') AS reason,
        COUNT(*)::INTEGER AS count,
        (SELECT COUNT(*) FROM base_conversations WHERE status = 'paused')::INTEGER AS total
      FROM base_conversations c
      LEFT JOIN conversation_reasons_pauses_and_closures r
        ON r.id = c.conversation_pause_reason_id
        AND r.reason_type = 'pause'
      WHERE c.status = 'paused'
      GROUP BY r.description
      ORDER BY count DESC
      LIMIT 10
    ) reasons
    WHERE total > 0
  ),

  -- ================================================================
  -- CLOSURE REASONS: Real data from database
  -- ================================================================
  closure_reasons AS (
    SELECT COALESCE(
      json_agg(
        json_build_object(
          'reason', reason,
          'count', count,
          'percentage', ROUND((count::DECIMAL / NULLIF(total, 0)) * 100, 1)
        )
        ORDER BY count DESC
      ),
      '[]'::json
    ) AS data
    FROM (
      SELECT
        COALESCE(r.description, 'Não especificado') AS reason,
        COUNT(*)::INTEGER AS count,
        (SELECT COUNT(*) FROM base_conversations WHERE status = 'closed')::INTEGER AS total
      FROM base_conversations c
      LEFT JOIN conversation_reasons_pauses_and_closures r
        ON r.id = c.conversation_closure_reason_id
        AND r.reason_type = 'closure'
      WHERE c.status = 'closed'
      GROUP BY r.description
      ORDER BY count DESC
      LIMIT 10
    ) reasons
    WHERE total > 0
  )

  -- ================================================================
  -- FINAL RESULT: Combine all sections
  -- ================================================================
  SELECT json_build_object(
    'kpis', (SELECT row_to_json(funnel_kpis.*) FROM funnel_kpis),
    'statusEvolution', (SELECT data FROM status_evolution),
    'pauseReasons', (SELECT COALESCE(data, '[]'::json) FROM pause_reasons),
    'closureReasons', (SELECT COALESCE(data, '[]'::json) FROM closure_reasons)
  ) INTO v_result;

  RETURN v_result;

EXCEPTION WHEN OTHERS THEN
  RAISE NOTICE 'Error in get_funil_data: %', SQLERRM;
  RETURN json_build_object(
    'error', SQLERRM,
    'kpis', json_build_object(
      'conversationsOpen', 0,
      'conversationsPaused', 0,
      'conversationsClosed', 0,
      'conversionRate', 0,
      'avgTimeToPauseSeconds', 0,
      'avgTimeToCloseSeconds', 0
    ),
    'statusEvolution', '[]'::json,
    'pauseReasons', '[]'::json,
    'closureReasons', '[]'::json
  );
END;
$function$
;

CREATE OR REPLACE FUNCTION public.get_open_conversations_by_tenant_and_ia_active(p_tenant_id uuid, p_ia_active boolean, p_consecutive_reactivations integer, p_max_inactive_minutes integer, p_limit integer)
 RETURNS SETOF public.conversations
 LANGUAGE sql
 STABLE
AS $function$
  select c.*
  from public.conversations c
  inner join public.contacts ct
    on ct.id = c.contact_id
  left join public.tenant_reactivation_settings trs_cfg
    on trs_cfg.tenant_id = c.tenant_id
  inner join lateral (
    select max(coalesce(m."timestamp", m.created_at)) as last_customer_message_at
    from public.messages m
    where m.conversation_id = c.id
      and m.sender_type = 'customer'
  ) lm
    on lm.last_customer_message_at is not null
  inner join lateral (
    select
      trs.sequence,
      trs.wait_time_minutes,
      trs.start_time,
      trs.end_time
    from public.tenant_reactivation_rules_steps trs
    where trs.tenant_id = c.tenant_id
      and trs.sequence = p_consecutive_reactivations
      and (
        trs.start_time is null
        or trs.end_time is null
        or (
          trs.start_time <= trs.end_time
          and (now() at time zone 'America/Fortaleza')::time between trs.start_time and trs.end_time
        )
        or (
          trs.start_time > trs.end_time
          and (
            (now() at time zone 'America/Fortaleza')::time >= trs.start_time
            or (now() at time zone 'America/Fortaleza')::time <= trs.end_time
          )
        )
      )
    order by trs.created_at asc
    limit 1
  ) step
    on true
  where c.tenant_id = p_tenant_id
    and c.status = 'open'
    and c.ia_active = p_ia_active
    and c.consecutive_reactivations = greatest(p_consecutive_reactivations - 1, 0)
    and lm.last_customer_message_at <= now() - make_interval(mins => step.wait_time_minutes)
    and lm.last_customer_message_at >= now() - make_interval(mins => p_max_inactive_minutes)
    and ct.is_muted is false
    and (
      coalesce(trs_cfg.reactivate_only_after_first_human_message, false) = false
      or exists (
        select 1
        from public.messages m_att
        where m_att.conversation_id = c.id
          and m_att.sender_type = 'attendant'
      )
    )
    and not exists (
      select 1
      from public.conversation_followups cf
      where cf.conversation_id = c.id
        and cf.is_done is false
    )
    and not exists (
      select 1
      from public.conversation_tags ctag
      inner join public.tags t
        on t.id = ctag.tag_id
      where ctag.conversation_id = c.id
        and t.tag_type::text = 'success'
    )
  order by
    lm.last_customer_message_at asc,
    c.updated_at asc,
    c.created_at asc
  limit greatest(coalesce(p_limit, 0), 0);
$function$
;

CREATE OR REPLACE FUNCTION public.get_tags_by_tenant(p_tenant_id uuid)
 RETURNS SETOF public.tags
 LANGUAGE sql
 STABLE
AS $function$
  select tg.*
  from public.tags tg
