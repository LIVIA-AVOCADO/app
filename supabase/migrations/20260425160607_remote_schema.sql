CREATE OR REPLACE FUNCTION public.get_conversation_interactions_text(p_conversation_id uuid)
 RETURNS text
 LANGUAGE sql
 STABLE
AS $function$
  with ordered_messages as (
    select
      row_number() over (
        order by m."timestamp" asc, m.created_at asc, m.id asc
      ) as rn,

      to_char(m."timestamp", 'DD/MM/YYYY - HH24:MI:SS') as formatted_timestamp,

      case
        when m.sender_agent_id is not null then 'agente_ia'
        when m.sender_user_id is not null then 'humano'
        when m.sender_channel_id is not null then 'canal'
        when lower(coalesce(m.sender_type::text, '')) in ('ai', 'agent', 'assistant') then 'agente_ia'
        when lower(coalesce(m.sender_type::text, '')) in ('user', 'attendant', 'human', 'contact', 'customer', 'lead') then 'humano'
        else coalesce(m.sender_type::text, 'desconhecido')
      end as sender_label,

      case
        when lower(coalesce(m.message_type::text, 'text')) = 'text' then coalesce(m.content, '')
        else '[' || coalesce(m.message_type::text, 'arquivo') || '] ' || coalesce(m.content, '')
      end as rendered_content

    from public.messages m
    where m.conversation_id = p_conversation_id
  )
  select coalesce(
    string_agg(
      '# mensagem ' || lpad(rn::text, 3, '0') || E'\n' ||
      formatted_timestamp || E'\n' ||
      sender_label || ': ' || rendered_content,
      E'\n\n'
      order by rn
    ),
    ''
  )
  from ordered_messages;
$function$
;

CREATE OR REPLACE FUNCTION public.get_conversations_eligible_for_timeout_closure()
